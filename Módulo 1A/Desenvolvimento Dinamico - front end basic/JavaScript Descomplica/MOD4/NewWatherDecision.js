
// Pode ser Sunny, Rainy ou Cloudy


let WatherForeCast = "Sunny";

if (WatherForeCast == `Rainy`) {
    console.log ('You have to go with an umbrella');
} else if (WatherForeCast == `Cloudy`) { // Se não se
    console.log (`Deve-se levar o guarda chuva, somente por segurança`);
} else { // se não
    console.log (`You don't have to go with an umbrella`);
}